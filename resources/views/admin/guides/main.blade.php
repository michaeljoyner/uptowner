    <section id="translations">
        <h3>Translations</h3>
        <ul>
            <li>For user submitted content (menu items, etc), there are input fields for both Chinese and English, and
                it is
                up to you to complete.
            </li>
            <li>If it makes sense in the situation, we will use the English content in place of any missing Chinese
                content.
            </li>
            <li>Should you wish to update or correct translations for non-user submitted content, please just contact
                Dymantic Design.
            </li>
        </ul>
    </section>
    <section id="menu">
        <h3>The Menu</h3>
        <p>The menu is organised into Categories, Groups and Items. A Menu Item (e.g. Uptowner Benny) will always belong
            to
            a Group (e.g. Eggs Benedict). A Group may then be assigned to a Category (e.g. Breakfasts).</p>
        <p>The top level, which is Categories, gets a navigation link on the Menu page on the site, and within each
            Category
            there may be several Groups, each with several Items.</p>
        <p>An image strip of menu items is shown for each category. If a category has less than three images, no image strip will be shown. If a category has less than six images, placeholder images will be used.</p>
    </section>
    <section id="items">
        <h3>Menu Items</h3>
        <ul>
            <li>They will only be shown on the site if they are published and their parent Groups and Categories are
                published.
            </li>
            <li>The price of a menu item must be a positive integer value.</li>
            <li>The Menu Item's image will be shown together with other images from items in that same Category.</li>
            <li>Deleting a Menu Item is permanent, so if you'd like to temporarily hide it from the site, just unpublish
                it.
            </li>
        </ul>
    </section>
    <section id="featured">
        <h3>Featured Menu Items</h3>
        <ul>
            <li>Up to four menu items may be featured on the site.</li>
            <li>Only the four most recently added (to featured list) items will be shown.</li>
        </ul>
    </section>
    <section id="groups">
        <h3>Menu Groups</h3>
        <ul>
            <li>They (and their Items) will only be shown on the site if they are assigned to a published Menu
                Category.
            </li>
            <li>They may only be deleted if they are empty (contain no Menu Items)</li>
            <li>Unpublishing a Menu Group will hide all its Menu Items on the site.</li>
            <li>The description is currently not used on the site.</li>
        </ul>
    </section>
    <section id="categories">
        <h3>Menu Categories</h3>
        <ul>
            <li>Unpublishing a Category will hide everything in that Category (all Groups and Items).</li>
            <li>Deleting a Category will merely "release" the Groups and their Items from that Category, it will not
                delete them all.
            </li>
        </ul>
    </section>
    <section id="specials">
        <h3>Specials</h3>
        <ul>
            <li>Specials will only be shown on the site if they are published and their end date has not expired.</li>
            <li>Upcoming specials (start date in the future) will be shown if published.</li>
            <li>Specials don't actually require a date (start or end), and having no end date means it will never be
                expired (so will always show on the site as long as it is published).
            </li>
            <li>Old specials simply will not show, so there is no need to delete them if you feel you may use it
                again.
            </li>
            <li>It is important to remember to add your image to the special.</li>
        </ul>
    </section>
    <section id="events">
        <h3>Events</h3>
        <ul>
            <li>Events must have an actual event date.</li>
            <li>Event times can be any text value (i.e. 9pm or 'until late', etc).</li>
            <li>A Featured Event can be chosen, and this is what will show on the main Events page banner.</li>
            <li>A Featured Event must have its own image.</li>
            <li>If no event is marked as featured, then the next upcoming event that has an image and is published will
                be displayed as the featured event.
            </li>
            <li>The "Coming Soon" events are the next three upcoming events that are published and have an image.</li>
            <li>All other published events will be shown in the "Upcoming Events" section.</li>
            <li>Once an event has expired it will no longer be shown. It may either be deleted or recycled for future
                use by editing the date (and using publish/unpublish to hide if necessary).
            </li>
        </ul>
    </section>
    <section id="users">
        <h3>Users</h3>
        <ul>
            <li>Users can only be added by other 'super' admin users.</li>
            <li>All users can (and should) reset their passwords once they have been registered.</li>
            <li>A new user may be registered as a 'Super' user or as a regular user. The rules that apply to each still
                need to be defined.
            </li>
        </ul>
    </section>
